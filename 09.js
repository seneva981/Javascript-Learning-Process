// Conditional Statements - if

// Example 01

let age = 16;

if (age > 18) {
    console.log("You can drive and vote.");
}

if (age < 18) {
    console.log("You cannot drive and vote.");
}

// Example 02

let mode = "light";
let color;

if (mode === "dark") {
    color = "black";
}

if (mode === "light") {
    color = "white";
}

console.log(color);