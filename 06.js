// Ternary Operators

let a = 4;

a++;
console.log(a);

a--;
console.log(a);

console.log("a = ", a++);                       // Value will be changed after printing the value of a
console.log("Value of a now: ", a);

console.log("a = ", ++a);                       // Value will be changed first and then print the changed value
console.log("Value of a still be: ", a);