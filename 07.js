// Assignment Operators

let a = 4;

a += 4;
console.log("Value of a after adding 4 is: ", a);

a -= 6;
console.log("Value of a after subtracting 6 is: ", a);

a *= 4;
console.log("Value of a after multiplying by 4 is: ", a);

a /= 4;
console.log("Value of a after dividing by 3 is: ", a);

a %= 4;
console.log("Remainder after dividing a by 4 is: ", a);

a **= 6;
console.log("Value of a after exponenting with 6 is: ", a);