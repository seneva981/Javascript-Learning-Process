// Arithematic Operators

let a = 5;
let b = 3;

console.log("a + b = ", a+b);
c = a-b;                                      // Storing result in another variable and then printing its value. This is another way to print the result.
console.log("a - b = ", c);
console.log("a * b = ", a*b);
d = a/b;
console.log("a / b = ", d);
console.log("a % b = ", a%b);
e = a ** b;                                 // Exponential operator
console.log("a ^ b = ", e);