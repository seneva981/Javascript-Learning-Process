// Comparison Operators

let f = 6;
let g = 8;
let h = 8;
let i = "8";

console.log(f, "==", g, ":", f==g);
console.log(f, "!=", g, ":", f!=g);
console.log(g, "==", h, ":", g==h);
console.log(g, "!=", h, ":", g!=h);
console.log(h, "==", i, ":", h==i);
console.log(h, "===", i, ":", h===i);
console.log(h, "!=", i, ":", h!=i);
console.log(h, "!==", i, ":", h!==i);
console.log(f, ">", g, ":", f>g);
console.log(f, "<", g, ":", f<g);
console.log(f, ">=", g, ":", f>=g);
console.log(g, "<=", h, ":", g<=h);